def get_prompt(player_name: str) -> str:
    if player_name not in TUNISIA_PROMPTS:
        available = "\n".join(f"  - {n}" for n in sorted(TUNISIA_PROMPTS))
        raise KeyError(f"'{player_name}' not found.\nAvailable:\n{available}")
    return TUNISIA_PROMPTS[player_name]


def list_squad() -> list[str]:
    return list(TUNISIA_PROMPTS.keys())
